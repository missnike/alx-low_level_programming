#include "3-calc.h"
/**
*op_add - function to add sum a and b
*@a: the first number
*@b: the second number
*Return: the sum of a and b
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
*op_sub - function to subtract a and b
*@a: the first number
*@b: the second number
*Return: the subtraction of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
*op_mul - function to multiply a and b
*@a: the first number
*@b: the second number
*Return: the multiplication of a and b
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
*op_div - function to divide sum a and b
*@a: the first number
*@b: the second number
*Return: the division of a and b
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
*op_mod - function to return the division of sum a and b
*@a: the first number
*@b: the second number
*Return: the reminder of a and b
*/
int op_mod(int a, int b)
{
	        return (a % b);
}
